/**
 * Parcourt assets/video/<dossier>/ (recursif) et génère src/data/generated/assetMediaMap.ts
 * — 1ère vidéo (tri naturel sur chemin relatif) = hero, puis images triées.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const srcRoot = path.join(root, 'assets', 'video');
const outDir = path.join(root, 'src', 'data', 'generated');
const outFile = path.join(outDir, 'assetMediaMap.ts');

const VIDEO_EXT = /\.(mp4|webm|mov)$/i;
const IMAGE_EXT = /\.(png|jpe?g|gif|webp|avif)$/i;

function naturalSort(a, b) {
  return a.localeCompare(b, undefined, { numeric: true, sensitivity: 'base' });
}

/** Chemins relatifs au dossier projet, avec des slashs URL. */
function collectRelativeFiles(dir, baseDir) {
  const out = [];
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, ent.name);
    if (ent.isDirectory()) {
      out.push(...collectRelativeFiles(full, baseDir));
    } else if (ent.isFile()) {
      out.push(path.relative(baseDir, full).split(path.sep).join('/'));
    }
  }
  return out;
}

function scanFolder(dirName) {
  const dir = path.join(srcRoot, dirName);
  const relPaths = collectRelativeFiles(dir, dir);
  const videos = relPaths.filter((f) => VIDEO_EXT.test(f)).sort(naturalSort);
  const images = relPaths.filter((f) => IMAGE_EXT.test(f)).sort(naturalSort);
  if (videos.length === 0) {
    console.warn(`[generate-asset-media] Aucune vidéo dans ${dirName}/ (attendu: .mp4, .webm ou .mov)`);
  }
  const video = videos[0] ? `/${dirName}/${videos[0]}` : null;
  const imagePaths = images.map((f) => `/${dirName}/${f}`);
  return { video, images: imagePaths };
}

if (!fs.existsSync(srcRoot)) {
  fs.mkdirSync(outDir, { recursive: true });
  const empty = `// Auto-generated — aucun dossier dans assets/video pour l'instant.
export const assetMediaMap = {} as const;
export type ProjectAssetFolder = never;
`;
  fs.writeFileSync(outFile, empty, 'utf8');
  console.log('assetMediaMap.ts (vide).');
  process.exit(0);
}

const folders = fs
  .readdirSync(srcRoot)
  .filter((n) => fs.statSync(path.join(srcRoot, n)).isDirectory())
  .sort(naturalSort);

const entries = {};
for (const name of folders) {
  entries[name] = scanFolder(name);
}

fs.mkdirSync(outDir, { recursive: true });

const keys = Object.keys(entries);

const body = keys
  .map((k) => {
    const { video, images } = entries[k];
    const vidStr = video ? JSON.stringify(video) : 'null';
    const imgStr =
      images.length === 0
        ? '[]'
        : `[\n${images.map((i) => `      ${JSON.stringify(i)},`).join('\n')}\n    ]`;
    return `  ${JSON.stringify(k)}: {\n    video: ${vidStr},\n    images: ${imgStr},\n  }`;
  })
  .join(',\n');

const inner = body ? `${body}\n` : '';
const ts = `// Auto-generated by scripts/generate-asset-media.mjs — ne pas modifier à la main.
// Ajoutez un dossier dans assets/video/<nom>/ puis: npm run sync:assets

export const assetMediaMap = {
${inner}} as const;

export type ProjectAssetFolder = keyof typeof assetMediaMap;
`;

fs.writeFileSync(outFile, ts, 'utf8');
console.log(`Généré ${outFile} (${keys.length} dossier(s)).`);
